package com.adicse.comercial.clases;

public class AcumuladoRutaDistribucionPeso {
	String centroPoblado;
	String itemDsc;
	String nombreId;
	String direccion;
	String nivel;
	String codigoModular;
	Double peso;
	
	public String getCentroPoblado() {
		return centroPoblado;
	}
	public void setCentroPoblado(String centroPoblado) {
		this.centroPoblado = centroPoblado;
	}
	public String getItemDsc() {
		return itemDsc;
	}
	public void setItemDsc(String itemDsc) {
		this.itemDsc = itemDsc;
	}
	public String getNombreId() {
		return nombreId;
	}
	public void setNombreId(String nombreId) {
		this.nombreId = nombreId;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	public String getCodigoModular() {
		return codigoModular;
	}
	public void setCodigoModular(String codigoModular) {
		this.codigoModular = codigoModular;
	}
	public Double getPeso() {
		return peso;
	}
	public void setPeso(Double peso) {
		this.peso = peso;
	}

	
	
	
}
